    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

		//Alta de la propiedad
		Propiedad p = null;

		//validacion
		if (txtDireccion.getText().equals("")) {
			JOptionPane.showMessageDialog(this, "La direccion no debe ser vacia");
			return;
		}

		String direccion = txtDireccion.getText();
		int cantHabitaciones = (int) spnCantHabitaciones.getValue();
		double precioBaseAlquiler = Double.parseDouble(txtPrecioBaseAlquiler.getText());
		boolean esUsoComercial = chkUsoComercial.isSelected();
		int cantInquilinos = (int) spnCantInquilinos.getValue();

		if (radCasa.isSelected()) {

		    boolean poseeGaraje = chkPoseeGarage.isSelected();
			p = new Casa(direccion, cantHabitaciones, precioBaseAlquiler, esUsoComercial, cantInquilinos, poseeGaraje);

		} else {

			double precioExpensas = Double.parseDouble(txtPrecioExpensas.getText());
			p = new Departamento(direccion, cantHabitaciones, precioBaseAlquiler, esUsoComercial, cantInquilinos, precioExpensas);
		}

		inmobiliaria.agregarPropiedad(p);
		this.dispose();
    }//GEN-LAST:event_btnAgregarActionPerformed

